// EditorCore.h
#pragma once
#include <string>
#include "Nessie/Core/TypeInfo.h"
#include "Nessie/Debug/Assert.h"
#include "Nessie/Graphics/ImGui/ImGuiUtils.h"

namespace nes::editor
{
    //----------------------------------------------------------------------------------------------------
    /// @brief : Generate a unique ID for ImGui.
    //----------------------------------------------------------------------------------------------------
    const char* GenerateID();

    //----------------------------------------------------------------------------------------------------
    /// @brief : Generate a unique ID for ImGui that uses the given label.
    //----------------------------------------------------------------------------------------------------
    const char* GenerateLabelID(std::string_view label);

    //----------------------------------------------------------------------------------------------------
    /// @brief : Check if Input is currently enabled for ImGui.
    //----------------------------------------------------------------------------------------------------
    bool        IsInputEnabled();

    //----------------------------------------------------------------------------------------------------
    /// @brief : Set whether input should be enabled for ImGui.
    //----------------------------------------------------------------------------------------------------
    void        SetInputEnabled(const bool enabled);

    //----------------------------------------------------------------------------------------------------
    /// @brief : Pushes to a new stack level for generating IDs.
    //----------------------------------------------------------------------------------------------------
    void        PushIDLevel();

    //----------------------------------------------------------------------------------------------------
    /// @brief : Pops to a previous ID stack level for generating IDs.
    //----------------------------------------------------------------------------------------------------
    void        PopIDLevel();

    //----------------------------------------------------------------------------------------------------
    /// @brief : Move the draw cursor on the X-Axis for ImGui draw calls. 
    //----------------------------------------------------------------------------------------------------
    void        ShiftCursorX(const float distance);
    
    //----------------------------------------------------------------------------------------------------
    /// @brief : Move the draw cursor on the Y-Axis for ImGui draw calls. 
    //----------------------------------------------------------------------------------------------------
    void        ShiftCursorY(const float distance);

    //----------------------------------------------------------------------------------------------------
    /// @brief : Move the draw cursor for ImGui draw calls. 
    //----------------------------------------------------------------------------------------------------
    void        ShiftCursor(const float xDist, const float yDist);

    //----------------------------------------------------------------------------------------------------
    /// @brief : Set a tooltip for the last rendered item. 
    //----------------------------------------------------------------------------------------------------
    void        SetToolTip(const char* toolTip);
}